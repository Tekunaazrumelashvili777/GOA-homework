fixed_number = 70  # წინასწარ განსაზღვრული რიცხვი

user_number = int(input("enter number: "))

if user_number > fixed_number:
    print("შეყვანილი რიცხვი მეტია 70-ზე.")
if user_number < fixed_number:
    print("შეყვანილი რიცხვი ნაკლებია 70-ზე.")
if user_number == fixed_number: 
    print("შეყვანილი რიცხვი ტოლია 70-ის.")